export function t(lang: Lang, key: string): string {
  return messages[lang]?.[key] ?? messages.uk?.[key] ?? key;
}

export function tf(
  lang: Lang,
  key: string,
  values: Record<string, string | number>,
): string {
  return Object.entries(values).reduce(
    (message, [name, value]) =>
      message.replaceAll(`{${name}}`, String(value)),
    t(lang, key),
  );
}
